#!/usr/bin/env python3
""" this module supplies a type-annotated function to_str 
    that takes a float n as argument and returns the string
    representation of the float
"""

def to_str(n: float) -> str:
    """ converts n into a float and return it
        n(float): float to conver
        return(string): string rep of n
    """
    return str(n)
